public class NumeroPositivoNegativo{
	public static void main(String args[]){
		int num=5;
		
		if(num>0){
			System.out.println("El numero es positivo");
		}
		else if(num<0){
			System.out.println("El numero es negativo");
		}
		else if(num==0){
			System.out.println("El numero vale 0");
		}
		
	}
		
	
}
	